/*Користувач вводить два цілі числа `m` і `n`, де `m` — кількість рядків, а `n` — кількість стовпців. Програма має вивести на екран прямокутник розміром `m` на `n`, повністю заповнений символами `*`.
*/
#include <iostream>

int main () {
int m, n;
std::cout <<"Кількість рядків:";
std::cin >> m;
std::cout <<"Кількість стовпців:";
std::cin >> n;
for (char k=0; k<m; k++)
{
	for (char i=0; i<n; i++)
		std::cout<< '*';
		std::cout << std::endl;
	
}
}
